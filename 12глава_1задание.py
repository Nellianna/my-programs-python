class Apple:
    def __init__(self, w, c, f, s):
        self.weight = w
        self.color = c
        self.form = f
        self.taste = s
        print("Создано!")

apl = Apple(50, "темное яблоко", "круг", "сладкое")
print(apl.weight)
print(apl.color)
print(apl.form)
print(apl.taste)

